package Java_Basics_2;

public class Keywords_and_Expressions {

	public static void main(String[] args) {

		// a mile = 1.609344 kms
		double kilometers = (100 * 1.609344);
		System.out.println("100 Mile = " + kilometers);

		// Excluding the keyword, everything in the brackets is an expression"()"
		int highScore = 50; // highScore = 50 is an expression
		if (highScore == 50) { // highScore == 50 is an expression
			System.out.println("This is an expression"); // "This is an expression" is an expression
		}

	}

}
